        public void setResults(long time,int dist, int contador, String ruta){
            jTextArea1.setText("");
            if(jRadioButton1.isSelected())
            jTextArea1.append("Busqueda en amplitud (BFS)\nSe generaron " + contador +" pasos para encontrar la solución\nLa Ruta es " +ruta);
            else{
                jTextArea1.append("Busqueda de Coste Uniforme (UCS)\nSe generaron " + contador +" pasos para encontrar la solución\nLa Ruta es " +ruta);               
                jTextArea1.append("\nLa distancia total recorrida es de " + dist + " km.");
            }
            jTextArea1.append("\nEl tiempo de ejecucion es de " + time +" ns");
        }

