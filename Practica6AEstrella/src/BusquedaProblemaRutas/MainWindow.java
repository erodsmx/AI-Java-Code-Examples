    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!jRadioButton1.isSelected() && !jRadioButton2.isSelected() && !jRadioButton3.isSelected()){
            JOptionPane.showMessageDialog(this, "Debes elegir un método de busqueda", "error", JOptionPane.INFORMATION_MESSAGE);
        }
        else if(jComboBox1.getSelectedItem() == "-----" || jComboBox2.getSelectedItem() == "-----"){
            JOptionPane.showMessageDialog(this, "Elige la ciudad faltante", "error", JOptionPane.INFORMATION_MESSAGE);
        }
        else if(jComboBox1.getSelectedItem() == jComboBox2.getSelectedItem()){
            JOptionPane.showMessageDialog(this, "No puedes elegir la misma ciudad", "error", JOptionPane.INFORMATION_MESSAGE);
        }
        else if(jRadioButton1.isSelected()){
            control.iniciaBFS();
        }
        else if(jRadioButton2.isSelected()){
            control.iniciaUCS();
        }
        else if(jRadioButton3.isSelected()){
            control.iniciaAE();
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed
